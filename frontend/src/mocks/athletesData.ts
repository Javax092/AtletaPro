export const athleteProfiles: AthleteProfile[] = squadAthletes.map((athlete) => ({
  athlete,
  automaticSummary: athlete.summary,
  observations: [
    `Sono médio recente em ${athlete.sleepHours.toFixed(1)}h com tendência ${athlete.trend === "up" ? "de alerta" : athlete.trend === "down" ? "favorável" : "estável"}.`,
    `Carga semanal projetada em ${athlete.workload} AU e fadiga subjetiva em ${athlete.fatigue.toFixed(1)}/10.`,
    athlete.minutesRestriction
      ? `Controle de minutos sugerido em até ${athlete.minutesRestriction} min.`
      : "Sem limitação objetiva de minutos neste momento.",
  ],
  recommendation: athlete.recommendation,
  recentHistory: athlete.history.map((point, index) => ({
    date: `2026-04-0${index + 1}`,
    sessionType: index >= 5 ? "Recuperação / jogo" : "Treino de campo",
    workload: point.load,
    fatigue: Number((athlete.fatigue + (index - 3) * 0.2).toFixed(1)),
    recovery: point.recovery,
    note:
      point.risk >= 0.6
        ? "Resposta abaixo do ideal para sequência intensa."
        : point.recovery >= 84
          ? "Boa prontidão na sessão seguinte."
          : "Sessão dentro do padrão competitivo.",
  })),
}));
